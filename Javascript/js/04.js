// String o cadenas de texto
const tweet = 'Aprendiendo JavaScript con el curso de desarrollo web completo"';
const producto2 = 'Monitor HD';

console.log(tweet.length);
console.log(producto2);

// IndexOf - Te dice en que posición se encuentra el texto que estás buscando
console.log(tweet.indexOf('JavaScript'));
console.log(producto2.indexOf('Tablet'));

// Includes - Regresa true o false sirve para saber si el texto se encuentra o no se encuentra en el string
console.log(tweet.includes('JavaScript'));
console.log(producto2.includes('Tablet'));